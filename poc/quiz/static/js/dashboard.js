$(async function () {
    const api_url = window.location.origin + 'api/all_charts' //generates url of backend api sending data of all charts
    const payload = await axios.get(api_url) //retrieving request payload
    const charts = payload.data.data //retrieving actual data from payload

    //iterates through each chart and sends a request to its corresponding query api url to retrieve data to display on chart
    for (let chart of charts) {
        let chartPayload = await axios.get(chart.url)
        let chartData = chartPayload.data.data
        let labels = []
        let dataSet = []
        // inserts the label for each summation of recommendations as a label and the actual # of recommendations as a data point
        chartData.forEach((data) => {
            //the label for AllProgramData is stored under key whereas the label for ProgramData is stored under program_position or program(if SUM count is selected)
            labels.push(data.key || (data.program_position || data.program))
            dataSet.push(data.total_recommendations)
        })

        //selects the specific canvas (each have the id of the chart's title) and creates a new Chart instance to insert into
        var ctx = document.getElementById(chart.title).getContext('2d');
        var myChart = new Chart(ctx, {
            type: chart.type,
            data: {
                labels: labels,
                datasets: [{
                    label: chart.title,
                    data: dataSet,
                    backgroundColor: [
                        'rgba(255, 99, 132, 0.2)',
                        'rgba(54, 162, 235, 0.2)',
                        'rgba(255, 206, 86, 0.2)',
                        'rgba(75, 192, 192, 0.2)',
                        'rgba(153, 102, 255, 0.2)',
                        'rgba(255, 159, 64, 0.2)',
                        'rgba(255, 99, 132, 0.2)',
                        'rgba(54, 162, 235, 0.2)',
                        'rgba(255, 206, 86, 0.2)',
                        'rgba(75, 192, 192, 0.2)',
                        'rgba(153, 102, 255, 0.2)',
                        'rgba(255, 159, 64, 0.2)',
                        'rgba(255, 99, 132, 0.2)',
                        'rgba(54, 162, 235, 0.2)',
                        'rgba(255, 206, 86, 0.2)',
                        'rgba(75, 192, 192, 0.2)',
                        'rgba(153, 102, 255, 0.2)',
                        'rgba(255, 159, 64, 0.2)',
                        'rgba(255, 99, 132, 0.2)',
                        'rgba(54, 162, 235, 0.2)',
                        'rgba(255, 206, 86, 0.2)',
                        'rgba(75, 192, 192, 0.2)',
                        'rgba(153, 102, 255, 0.2)',
                        'rgba(255, 159, 64, 0.2)'
                    ],
                    borderColor: [
                        'rgba(255, 99, 132, 1)',
                        'rgba(54, 162, 235, 1)',
                        'rgba(255, 206, 86, 1)',
                        'rgba(75, 192, 192, 1)',
                        'rgba(153, 102, 255, 1)',
                        'rgba(255, 159, 64, 1)',
                        'rgba(255, 99, 132, 1)',
                        'rgba(54, 162, 235, 1)',
                        'rgba(255, 206, 86, 1)',
                        'rgba(75, 192, 192, 1)',
                        'rgba(153, 102, 255, 1)',
                        'rgba(255, 159, 64, 1)',
                        'rgba(255, 99, 132, 1)',
                        'rgba(54, 162, 235, 1)',
                        'rgba(255, 206, 86, 1)',
                        'rgba(75, 192, 192, 1)',
                        'rgba(153, 102, 255, 1)',
                        'rgba(255, 159, 64, 1)'
                    ],
                    borderWidth: 1
                }]
            },
            options: {
                responsive: false,
                scales: {
                    yAxes: [{
                        ticks: {
                            beginAtZero: true
                        }
                    }]
                }
            }
        });
    }
})